    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        String sql  = "select * from Log_In where  user_name = ? and password = ?";
        try {
            pst = connection.prepareStatement(sql);
            pst.setString(1, jTextFieldusername.getText());
            pst.setString(2,jPasswordField1password.getText());
            res = pst.executeQuery();
            if (res.next())
            {
                close();
                EasyStat e = new EasyStat();
                e.setVisible(true);
                
                try {
                    res.close();
                    pst.close();
                    connection.close();
                } catch (SQLException ex) {
                    
                    JOptionPane.showMessageDialog(rootPane, ex);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Username or password is incorrect");
                jTextFieldusername.setText("");
                jPasswordField1password.setText("");
            }
            
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_LoginActionPerformed

